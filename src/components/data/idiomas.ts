const esCodigoIdioma = (codigo: string | null): codigo is CodigoIdioma => {
  return idiomas.some(idioma => idioma.codigo === codigo)
}

const idiomaGuardado = localStorage.getItem('idioma')
const idiomaInicial: CodigoIdioma = esCodigoIdioma(idiomaGuardado)
  ? idiomaGuardado
  : 'cast'

export const idiomaActivo = ref<CodigoIdioma>(idiomaInicial)

export const cambiarIdioma = (codigo: CodigoIdioma) => {
  idiomaActivo.value = codigo
  localStorage.setItem('idioma', codigo)
}

export const t = (clave: string) => {
  const partes = clave.split('.')
  let valor: unknown = traducciones[idiomaActivo.value]

  for (const parte of partes) {
    if (!valor || typeof valor !== 'object' || !(parte in valor)) {
      return clave
    }

    valor = (valor as Record<string, unknown>)[parte]
  }

  return typeof valor === 'string' ? valor : clave
}

